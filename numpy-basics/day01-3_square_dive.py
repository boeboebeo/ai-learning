# square 시각화 

import numpy as np
import matplotlib.pyplot as plt

#Sqaure 주기 
T = 1.0 #한 사이클이 완료되는데 걸리는 시간. 여기서는 1초이기 때문에 1Hz 
omega = 2 * np.pi / T   # ω : (각주파수, Angular Frequency). 1초에 몇 라디안씩 회전하는지?
                        # 그럼 2pi/T 면 1초동안 2pi/T 라디안 회전 = 약 3.16*2 = 6.28rad/s : 1초에 2pi radian 회전
                        # T가 낮으면 omega가 커짐. (주파수가 높은것. 빨리 진동)
t = np.linspace(0, T, 1000) #0부터 T까지를 1000으로 나눔 

# Fourier Series 로 Sqaure wave 근사
def square_fourier(t, num_harmonics, with_scaling=True):
    result = np.zeros_like(t)
        #t와 같은 크기의 0으로만 채워진 배열을 만듦. [1, 2, 3, 4, 5] => [0, 0, 0, 0, 0] . t의 크기가 바뀌어도 자동으로 맞춤
    for k in range(1, num_harmonics + 1): #1부터 50까지의 k
        n = 2*k -1 #홀수만
        result += np.sin(n * omega * t) / n 
        # omega = 2*np.pi / T  ex. T=1 이라면 2pi rad/s (각속도)
        # t = [0, 0.1, 0.2, 0.3, ...] 시간 배열
        # n=1일때 t의 배열 각각과 곱해진다면 => np.sin(1 * 2π * t) = [0, 0.588, 0.951, 0.951, ...]
        # n=3일때 t의 배열 각각과 곱해진다면 => np.sin(3 * 2π * t) = [0, 0.951, -0.588, -0.588, ...]
        # 그 후 /n 은 각 n차 harmonic의 진폭을 줄이는데 사용됨 

    if with_scaling:
        return (4/np.pi) * result
    else:
        return result 
    
# 비교
no_scaling = square_fourier(t, 1000, with_scaling=False) #여기서 50이 num_harmonics 
with_scaling = square_fourier(t, 1000, with_scaling=True)

plt.figure(figsize=(12, 6))

plt.subplot(1, 2, 1)
plt.plot(t, no_scaling)
plt.axhline(y=np.pi/4, color='r', linestyle='--', label=f'max = π/4 ≈ {np.pi/4:.3f}')
plt.ylim(-1.5, 1.5)
plt.title('without 4/π')
plt.legend()
plt.grid(True, alpha=0.3)

plt.subplot(1, 2, 2)
plt.plot(t, with_scaling)
plt.axhline(y=1, color='g', linestyle='--', label='max=1.0')
plt.title('with *4/π')
plt.legend()
plt.grid(True, alpha=0.3)

plt.tight_layout()
plt.show()

print(f"max without scaling : {np.max(no_scaling):.4f}")
print(f"max with scaling : {np.max(with_scaling):.4f}")



